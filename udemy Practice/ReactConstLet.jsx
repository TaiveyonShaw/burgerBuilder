// Let allows you to change the variable
let Name = 'Taiveyon';
console.log(Name);

Name = "Billy bob joe";
console.log(Name);

// Const keeps the variable constant where it is unable to change
const printmyName = (thename) => console.log(thename);

printmyName("Taiveyon");

const multiply = (number) => number * 2;

console.log(multiply(2));